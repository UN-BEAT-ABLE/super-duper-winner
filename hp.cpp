#include<iostream>
using namespace std;
int main()
{
  // declare variables
  float p, t, r, interest;

  // take input from end-user
  cout << "Enter principal amount, time and rate:";
  cin >> p >> t >> r;

  // calculate interest
  interest = (p*t*r)/100;

  // display result
  cout << "Interest = " << interest << endl;

  return 0;
}
